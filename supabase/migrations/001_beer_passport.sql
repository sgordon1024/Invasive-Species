-- =============================================================================
-- Helper: increment_visits
-- Called inside the checkin Edge Function to atomically bump total_visits.
-- =============================================================================
CREATE OR REPLACE FUNCTION increment_visits(p_member_id UUID)
RETURNS INTEGER
LANGUAGE plpgsql
SECURITY DEFINER  -- runs with owner privileges so RLS doesn't block it
AS $$
DECLARE
  new_count INTEGER;
BEGIN
  UPDATE passport_members
  SET total_visits = total_visits + 1
  WHERE id = p_member_id
  RETURNING total_visits INTO new_count;

  RETURN new_count;
END;
$$;


-- =============================================================================
-- Helper: get_member_for_staff
-- Lets staff look up a member by email, phone, or member_id without exposing
-- the full members table publicly.
-- =============================================================================
CREATE OR REPLACE FUNCTION get_member_for_staff(p_query TEXT)
RETURNS TABLE (
  id           UUID,
  first_name   TEXT,
  email        TEXT,
  phone        TEXT,
  member_id    TEXT,
  total_visits INTEGER,
  is_staff     BOOLEAN,
  created_at   TIMESTAMPTZ
)
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  RETURN QUERY
  SELECT
    m.id, m.first_name, m.email, m.phone,
    m.member_id, m.total_visits, m.is_staff, m.created_at
  FROM passport_members m
  WHERE
    m.email    = LOWER(TRIM(p_query)) OR
    m.phone    = TRIM(p_query)        OR
    m.member_id = UPPER(TRIM(p_query));
END;
$$;
